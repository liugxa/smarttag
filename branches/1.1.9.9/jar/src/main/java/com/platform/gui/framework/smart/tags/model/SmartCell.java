package com.platform.gui.framework.smart.tags.model;

import com.platform.gui.framework.smart.inter.IColumnDecorate;

public class SmartCell {

	private String key;
	
	private String name;

	private String label;
	
	private String value;

	private Boolean disable;
	
	private Boolean escape;
	
	private String decorate;
	
	private String parameters;
	
	/* right/left/middle/top/bottom */
	private String align;
	
	private String style;
	
	private IColumnDecorate columnDecorate;
	
	/** @deprecated url has been replaced by parameters*/
	private String url;
	
	/** @deprecated url has been replaced by parameters*/
	private String format;
	
	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public Boolean getDisable() {
		return disable;
	}

	public void setDisable(Boolean disable) {
		this.disable = disable;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getDecorate() {
		return decorate;
	}

	public void setDecorate(String decorate) {
		this.decorate = decorate;
	}

	public IColumnDecorate getColumnDecorate() {
		return columnDecorate;
	}

	public void setColumnDecorate(IColumnDecorate columnDecorate) {
		this.columnDecorate = columnDecorate;
	}

	public Boolean getEscape() {
		return escape;
	}

	public void setEscape(Boolean escape) {
		this.escape = escape;
	}

	public String getParameters() {
		return parameters;
	}

	public void setParameters(String parameters) {
		this.parameters = parameters;
	}
	
	public String getAlign() {
		return align;
	}

	public void setAlign(String align) {
		this.align = align;
	}	
	
	public String getStyle() {
		return style;
	}

	public void setStyle(String style) {
		this.style = style;
	}	
}
