package com.platform.gui.framework.smart.tags.model;

public class SmartButton {

	private String name;
	
	private String label;

	public SmartButton(){}
	
	public SmartButton(String label){
		this.label = label;
	}
	
	public SmartButton(String name , String label){
		this.name = name;
		this.label = label;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}
}
